def addition(num1, num2)
  sum = (num1 + num2)
  return sum
end


def subtraction(num1, num2)
  difference = (num1 - num2)
  return difference
end

def division(num1, num2)
  quotient = (num1 / num2)
  return quotient
end

def multiplication(num1, num2)
  product = (num1 * num2)
  return product
end

def modulo(num1, num2)
  remainder = (num1 % num2)
  return remainder
end

def square_root(num)
  square_root = (num * num)
  return square_root
end
